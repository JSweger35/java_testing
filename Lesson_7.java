public class Lesson_7 {
    /**
     * main() reads Fahrenheit temperatures and 
     * displays their Celsius and Kelvin equivalents.
     */
    public static void main(String[] args) {
        int x = 0;
        do {
            System.out.println(x);
            x++;
        } while(x < 10);
        int y = 0;
        while (y < 10){
            System.out.println(y);
            y++; 
        }
    }
}
